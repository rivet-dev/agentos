#!/usr/bin/env node
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { execFileSync } from "node:child_process";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const sourcePath = resolve(repoRoot, "docs-internal/node-runtime-wasm-abi/agentos-posix-contract.json");
const outputPath = resolve(repoRoot, "crates/wasm-posix-host/src/generated.rs");
const check = process.argv.slice(2).includes("--check");
if (process.argv.slice(2).some((argument) => argument !== "--check")) {
	throw new Error("usage: generate-wasm-posix-host-table.mjs [--check]");
}

const contract = JSON.parse(readFileSync(sourcePath, "utf8"));
if (contract.schema !== 1 || contract.namespace !== "agentos_posix_v1") {
	throw new Error("unsupported POSIX contract");
}

function variant(name) {
	return name.split(/[^A-Za-z0-9]+/).map((part) => part[0].toUpperCase() + part.slice(1)).join("");
}

function types(signature, section) {
	const match = new RegExp(`\\(${section} ([^)]+)\\)`).exec(signature);
	return match ? match[1].trim().split(/\s+/) : [];
}

function rustType(type) {
	const mapped = { i32: "WasmType::I32", i64: "WasmType::I64", f32: "WasmType::F32", f64: "WasmType::F64" }[type];
	if (!mapped) throw new Error(`unsupported WASM type: ${type}`);
	return mapped;
}

const entries = [...contract.entries].sort((left, right) => left.name.localeCompare(right.name));
const variants = entries.map((entry, index) => `    ${variant(entry.name)} = ${index},`).join("\n");
const descriptors = entries.map((entry) => {
	const params = types(entry.signature, "param").map(rustType).join(", ");
	const results = types(entry.signature, "result").map(rustType).join(", ");
	return `    SyscallDescriptor { id: SyscallId::${variant(entry.name)}, name: ${JSON.stringify(entry.name)}, params: &[${params}], results: &[${results}], result_classification: ${JSON.stringify(entry.resultClassification)}, authorization_rule: ${JSON.stringify(entry.authorizationRule)}, test_id: ${JSON.stringify(entry.testId)} },`;
}).join("\n");

const unformatted = `// @generated by scripts/generate-wasm-posix-host-table.mjs; do not edit.\nuse crate::WasmType;\n\n#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]\n#[repr(u16)]\npub enum SyscallId {\n${variants}\n}\n\n#[derive(Debug, Clone, Copy, PartialEq, Eq)]\npub struct SyscallDescriptor {\n    pub id: SyscallId,\n    pub name: &'static str,\n    pub params: &'static [WasmType],\n    pub results: &'static [WasmType],\n    pub result_classification: &'static str,\n    pub authorization_rule: &'static str,\n    pub test_id: &'static str,\n}\n\npub static SYSCALLS: &[SyscallDescriptor] = &[\n${descriptors}\n];\n\nimpl SyscallId {\n    pub fn descriptor(self) -> &'static SyscallDescriptor {\n        &SYSCALLS[self as usize]\n    }\n\n    pub fn from_name(name: &str) -> Option<Self> {\n        match name {\n${entries.map((entry) => `            ${JSON.stringify(entry.name)} => Some(Self::${variant(entry.name)}),`).join("\n")}\n            _ => None,\n        }\n    }\n}\n`;
const output = execFileSync("rustfmt", ["--edition", "2021"], {
	input: unformatted,
	encoding: "utf8",
});

if (check) {
	if (!existsSync(outputPath) || readFileSync(outputPath, "utf8") !== output) {
		throw new Error(`generated WASM POSIX provider table is stale: ${outputPath}`);
	}
} else {
	writeFileSync(outputPath, output);
}
process.stdout.write(`WASM POSIX Rust provider table ${check ? "verified" : "generated"}: ${entries.length} syscalls\n`);
